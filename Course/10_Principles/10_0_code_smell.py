# Code smell.
# - Duplikált kód
# - A kód nem tesztelhető
# - A kód nehezen karbantartható, bővíthető, módosítható, olvasható

# Magic numbers (varázsszámok)
# - A varázsszámok olyan számok, amelyek a kódban szó szerint szerepelnek.
# - A varázsszámok nehezen értelmezhetőek és karbantarthatatlanok.
# - A varázsszámokat konstansokkal kell helyettesíteni.
