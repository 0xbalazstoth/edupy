# Kód tesztelése:
# - Olyan folyamat, ami során ellenőrizzük, hogy a program helyesen működik-e, és
#   a várt eredményeket adja vissza.
# - A tesztelés célja, hogy megtaláljuk a hibákat (bugokat), enélkül sérül az alkalmazás megbízhatósága
#   a felhasználók felé.

# Tesztelés típusai:

# Egység tesztelés (Unit Testing):
# - Egyes kódrészeket teszteljük (általában függvényeket), hogy helyesen működnek-e.
#   Ez a legkisebb tesztelési egység.

# Integrációs teszt (Integration Testing):
# - Különböző kódrészek együttműködésének tesztelése, hogy megbizonyosodjunk arról, hogy a különálló
#   modulok együtt is helyesen működnek.

# Rendszer tesztelés (System Testing):
# - Az egész rendszer tesztelése annak biztosítására, hogy az összes komponens együttesen
#   megfelelően működik.

# Elfogadási tesztelés (Acceptance Testing):
# - A végfelhasználók által végzett tesztelés, hogy a rendszer megfelel-e az
#   elvárásoknak és követelményeknek.

# Fejlesztési irányok
# TDD (Test-Driven Development) alapú fejlesztés
# BDD (Behavioral-Driven Development) alapú fejlesztés
# ATDD (Acceptance Test-Driven Development) alapú fejlesztés
